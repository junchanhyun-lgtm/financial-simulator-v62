def build_sim_results_state(
    years,
    main_pv,
    main_nom,
    main_returns,
    n_sims,
    safe_extra,
    trimmed_avg_extra,
    base_ruin,
    stress_df,
    dwz_mode,
    t_ruin,
    defense_rate,
    clean_lump_df,
    retire_age,
    shock_mask=None,
    inflation_matrix=None,
    quant_penalty=None,
    spending_pv=None,
    net_cashflow_pv=None,
    withdrawal_pv=None,
    return_floor_mask=None,
    return_ceiling_mask=None,
    market_index=None,
    raw_returns=None,
    return_assumption_info=None,
    sensitivity_df=None,
    scenario_comparison_df=None,
):
    return {
        "years": years,
        "pv": main_pv,
        "nom": main_nom,
        "returns": main_returns,
        "raw_returns": raw_returns,
        "return_assumption_info": return_assumption_info or {},
        "n_sims": n_sims,
        "safe_extra": safe_extra,
        "trimmed_avg_extra": trimmed_avg_extra,
        "base_ruin": base_ruin,
        "stress_df": stress_df,
        "dwz_mode": dwz_mode,
        "t_ruin": t_ruin,
        "defense_rate": defense_rate,
        "lump_df": clean_lump_df,
        "retire_age": retire_age,
        "shock_mask": shock_mask,
        "inflation_matrix": inflation_matrix,
        "quant_penalty": quant_penalty,
        "spending_pv": spending_pv,
        "net_cashflow_pv": net_cashflow_pv,
        "withdrawal_pv": withdrawal_pv,
        "return_floor_mask": return_floor_mask,
        "return_ceiling_mask": return_ceiling_mask,
        "market_index": market_index,
        "sensitivity_df": sensitivity_df,
        "scenario_comparison_df": scenario_comparison_df,
    }
